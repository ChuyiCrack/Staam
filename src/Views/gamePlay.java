    private void closeGameplay(){
        int minutes = this.thread.getSeconds() / 60;
        this.thread.stopThread();  // signal thread to stop
        
        
        try{
            Connection conn = Database.getConnection();
            PreparedStatement stmt;
            if(minutes > 0){
                stmt = conn.prepareStatement("UPDATE SesionJuego SET DuracionSesion = ? WHERE ID_Sesion = ?");
                stmt.setInt(1, minutes);
                stmt.setInt(2, this.idSession);
                
            }
            else{
                stmt = conn.prepareStatement("DELETE FROM SesionJuego WHERE ID_Sesion = ?");
                stmt.setInt(1, this.idSession);
            }
            stmt.executeUpdate();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
         }
        
    }

