    public gamePlay(int idGame , int idSession) {
        initComponents();
        setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                closeGameplay();
                dispose();
            }

            @Override
            public void windowClosed(WindowEvent e) {
                closeGameplay();
                windowCreator.openJframeWindow(new GameBInfo(idGame), gameName);
            }
        });
        this.idGame = idGame;
        this.idSession = idSession;
        String query = "SELECT * FROM Juegos WHERE ID_Juegos = ?";
        Color[] colors = {
            Color.RED,
            Color.BLUE,
            Color.GREEN,
            Color.YELLOW,
            Color.ORANGE,
            Color.PINK,
            Color.CYAN,
            Color.MAGENTA,
            Color.LIGHT_GRAY,
            Color.DARK_GRAY
        };

        // Create a Random object
        Random rand = new Random();

        // Pick a random color from the array
        Color randomColor = colors[rand.nextInt(colors.length)];
        
        for (Component comp : textPanel.getComponents()) {
            if (comp instanceof JLabel jLabel) {
                jLabel.setForeground(randomColor);
            }
            else if((comp instanceof JButton jButton)){
                jButton.setForeground(randomColor);
            }
        }
        
        try{
            Connection conn = Database.getConnection();
             PreparedStatement stmt = conn.prepareStatement(query);
             stmt.setInt(1,idGame);
             ResultSet rs = stmt.executeQuery();
             rs.next();
             this.gameName = rs.getString("Nombre");
             GameName.setText(this.gameName );
        }
         catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
         }
        
        timerThread timer = new timerThread(timerPanel);
        this.thread = timer;
        timer.start();
    }
    
